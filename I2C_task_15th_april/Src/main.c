void gotoSleep(void)
{
	//enable the PWR control clock
	RCC->APB1ENR|= (RCC_APB1ENR_PWREN);
	
	//set the sleepdeep bit in system control register
	SCB->SCR |= SCB_SCR_SLEEPDEEP_Msk;
	
	//select stanby mode
	PWR->CR |= PWR_CR_PDDS;
	
	//clear wake up flag 
	PWR->CR |= PWR_CR_CWUF;
	
	//enable wake up pin
	PWR->CSR |= (PWR_CSR_EWUP1);
	
	//request wait for interrupt
	__WFI();
	
}	
